function applyLanguage(lang) {
  const dict = I18N[lang] || I18N.pt;
  document.documentElement.lang = lang === "en" ? "en" : "pt-BR";

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.dataset.i18n;
    const value = dict[key];
    if (value === undefined) return;
    if (el.dataset.i18nAttr) {
      el.setAttribute(el.dataset.i18nAttr, value);
    } else if (el.dataset.i18nHtml === "true") {
      el.innerHTML = value;
    } else {
      el.textContent = value;
    }
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    const value = dict[el.dataset.i18nPlaceholder];
    if (value) el.setAttribute("placeholder", value);
  });

  const metaDesc = document.querySelector('meta[name="description"]');
  if (metaDesc && dict["meta.description"]) metaDesc.content = dict["meta.description"];

  document.title =
    lang === "en"
      ? "Kaique Rocha · Software Engineer"
      : "Kaique Rocha · Engenheiro de Software";

  document.querySelectorAll(".lang-btn").forEach((btn) => {
    btn.classList.toggle("active", btn.dataset.lang === lang);
    btn.setAttribute("aria-pressed", btn.dataset.lang === lang);
  });

  localStorage.setItem("portfolio-lang", lang);
}

function initI18n() {
  const saved = localStorage.getItem("portfolio-lang");
  const lang = saved === "en" ? "en" : "pt";
  applyLanguage(lang);

  document.querySelectorAll(".lang-btn").forEach((btn) => {
    btn.addEventListener("click", () => applyLanguage(btn.dataset.lang));
  });
}

document.addEventListener("DOMContentLoaded", initI18n);
